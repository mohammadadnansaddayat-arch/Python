text = input("Enter a string: ")

print("1.Length: ", len(text))

print("2.Uppercase: ", text.upper())

print("3.Lowercase: ", text.lower())

print("4.Cpitalized: ", text.capitalize())

print("5.Titlecase: ", text.title())

print("6.Replace: ", text.replace("people","boy"))

print("7.Split: ", text.split())


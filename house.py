import turtle

screen = turtle.Screen()
screen.setup(width=1000,height=650)
screen.bgcolor("#7BE5F8")

pen = turtle.Turtle()
pen.hideturtle()
pen.speed(0)

pen.penup()
pen.goto(320,200)
pen.color("#FFD700")
pen.begin_fill()
pen.pendown()
pen.circle(60)
pen.end_fill()

def draw_mountain(x, y, width, height, color):
    pen.penup()
    pen.goto(x,y)
    pen.color(color)
    pen.begin_fill()
    pen.pendown()
    pen.goto(x + width / 2, y + height)
    pen.goto(x + width, y)
    pen.goto(x, y)
    pen.end_fill()

draw_mountain(-500, -100, 350, 220, "#4B6B3F")
draw_mountain(-250, -100, 400, 270, "#6B8E23")
draw_mountain(0, -100, 350, 250, "#4C5A32")
draw_mountain(250, -100, 400, 230, "#556B2F")

pen.penup()
pen.goto(-500, -100)
pen.color("#6bd66b")
pen.begin_fill()
pen.pendown()
pen.goto(500, -100)
pen.goto(500, -300)
pen.goto(-500, -300)
pen.goto(-500, -100)
pen.end_fill()

